# A list is an ordered collection of data
# List are mutable you an add and delete stuff

items = ["pen" , "ruler", "laptop ", "mathematical set"] # box brackets used for lists
print(items)
#append
#insert
#remove
#pop
# add
#index
#reverse
#count
#sort

#APPEND = adds things to the list
items.append("phone")
print("\n")
print(items)


#POP = removes last thing on the list
items.pop()
print("\n")
print(items)



#COUNT
items.count()
print("\n")
print(items)


#REMOVE
items.remove("ruler")
print("\n")
print(items)




#REVERSE
items.reverse()
print("\n")
print(items)




#SORT
items.sort()
print("\n")
print(items)
